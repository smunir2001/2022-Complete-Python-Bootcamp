print('1 > 2 --> ', 1 > 2)
print('1 < 2 --> ', 1 < 2)
print('1 == 2 --> ', 1 == 2)
print('1 >= 2 --> ', 1 >= 2)
print('1 <= 2 --> ', 1 <= 2)
print()

# comparing strings
word1 = 'hello'
word2 = 'bye'
print(word1 + ' == ' + word2 + ' --> ', word1 == word2)
print()

# comparing types
a = 2
b = 2.0
print(a, ' == ', b, ' --> ', a == b)
print()

# chaining comparison operators
print('1 > 2 --> ', 1 > 2)
print('2 < 3 --> ', 2 < 3)
print('1 < 2 > 3 --> ', 1 < 2 > 3)
print()
print('1 < 2 and 2 > 3 --> ', 1 < 2 and 2 > 3)
print('100 == 1 or 2 == 200 --> ', 100 == 1 or 2 == 200)
print('not 1 == 1 -->', not 1 == 1)